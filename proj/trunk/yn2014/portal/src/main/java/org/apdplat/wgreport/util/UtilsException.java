package org.apdplat.wgreport.util;

public class UtilsException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7542855452686906669L;

	public UtilsException() {
		super();
	}

	public UtilsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UtilsException(String arg0) {
		super(arg0);
	}

	public UtilsException(Throwable arg0) {
		super(arg0);
	}

}
